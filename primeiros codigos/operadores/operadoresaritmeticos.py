"""Soma (+): soma dois valores.
Subtração (-): subtrai o segundo valor do primeiro.
Multiplicação (*): multiplica dois valores.
Divisão (/): divide o primeiro valor pelo segundo e devolve um resultado de tipo flutuante.
Divisão inteira (//): divide o primeiro valor pelo segundo e devolve um resultado de tipo inteiro (a parte decimal é descartada).
Módulo (%): devolve o resto da divisão entre o primeiro valor e o segundo.
Exponenciação (**): eleva o primeiro valor à potência do segundo."""

a = 10
b = 3

print("Soma:", a + b)               # Soma
print("Subtração:", a - b)          # Subtração
print("Multiplicação:", a * b)       # Multiplicação
print("Divisão:", a / b)             # Divisão (com resultado float)
print("Divisão Inteira:", a // b)    # Divisão Inteira a parte decimal é descartada
print("Módulo:", a % b)              # Módulo devolve o resto da divisão entre o primeiro valor e o segundo
print("Exponenciação:", a ** b)      # Exponenciação 
# Operadores Aritméticos em Python
